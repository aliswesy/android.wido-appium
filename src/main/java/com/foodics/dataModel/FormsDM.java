package com.foodics.dataModel;

public class FormsDM {
    private String inputText;
    private String switchMessage;
    private String dropDownOption;

    public String getInputText() {
        return inputText;
    }

    public void setInputText(String inputText) {
        this.inputText = inputText;
    }

    public String getSwitchMessage() {
        return switchMessage;
    }

    public void setSwitchMessage(String switchMessage) {
        this.switchMessage = switchMessage;
    }

    public String getDropDownOption() {
        return dropDownOption;
    }

    public void setDropDownOption(String dropDownOption) {
        this.dropDownOption = dropDownOption;
    }

}
